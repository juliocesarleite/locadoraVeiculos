    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String pesquisa;
        
        acessoBD = new AcessoBD();

        acessoBD.conecta();

        pesquisa = jTextField1.getText();
        
        pedidoSQL2 = "SELECT veiculoprograma.MarcaVeicu, veiculoprograma.ModeloVeicu, clienteprograma.NomeCliente, clientexveiculo.formapgto, clientexveiculo.parcelas, clientexveiculo.ValorFinal FROM veiculoprograma,clienteprograma,clientexveiculo where ((clientexveiculo.codvenda='"+ jTextField1.getText()+ "') and (clienteprograma.CodCliente = clientexveiculo.CodCliente) and (veiculoprograma.CodVeiculo = clientexveiculo.CodVeiculo))";

        rs = acessoBD.consulta(pedidoSQL2);

        
        try {
            while(rs.next()){
                MarcaVeicu = rs.getString("MarcaVeicu");
                ModeloVeicu = rs.getString("ModeloVeicu");
                NomeCliente = rs.getString("NomeCliente");
                formapgto = rs.getString("formapgto");
                parcelas = rs.getString("parcelas");
                ValorFinal = rs.getString("ValorFinal");

                jTextField2.setText("" + NomeCliente);
                jTextField7.setText("" + MarcaVeicu);
                jTextField3.setText("" + ModeloVeicu);
                jTextField4.setText("" + formapgto);
                jTextField5.setText("" + parcelas);
                jTextField6.setText("" + ValorFinal);
            }
            } catch (SQLException e) {
            System.out.println("ExceÃ§Ã£o: " + e.toString());
        }
        acessoBD.fechaConexao();
    }//GEN-LAST:event_jButton1ActionPerformed

